# 2231번 분해합
# 분해합은 또 뭐여 
# 그러니까 각자리수 + 원래 자신 하면 된다는거야??
# 근데 어떻게 생성자가 없을 수가 있지?  

n = int(input())

result = 0  # 생성자를 못 찾았을 때를 대비해 기본값을 0으로 둡니다.

# 1부터 n까지 작은 수부터 차례대로 검사 (가장 작은 생성자를 찾기 위해)
for i in range(1, n + 1):
    
    # 1. 분해합 구하기 (i + 각 자리수의 합)
    # str(i)로 문자로 바꾼 뒤 map(int, ...)로 다시 숫자로 쪼개서 더함
    a = list(map(int, str(i))) 
    s = i + sum(a)
    
    # 2. 분해합이 입력받은 n과 같은지 확인
    if s == n:
        result = i  # 찾았다! i가 바로 생성자
        break       # 가장 작은 것을 찾았으니 더 볼 필요 없이 종료

print(result)